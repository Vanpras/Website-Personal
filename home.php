<h1><u>Latar Belakang Kubis</u></h1>
<p class="indent"> 
    Kubis merupakan salah satu jenis sayuran yang banyak dikonsumsi karena berbagai manfaat yang terdapat di dalam
    kubis. Kubis dikenal sebagai sumber vitamin A, B, dan C, mineral, karbohidrat, dan protein yang berguna bagi
    kesehatan. Seperti beberapa jenis sayuran lainnya, kubis memiliki sifat mudah rusak, dan tidak tahan disimpan lama.
    Sifat mudah rusak ini dapat disebabkan oleh daun yang lunak dan kandungan air cukup tinggi, sehingga mudah ditembus
    oleh alat-alat pertanian dan hama atau penyakit tanaman. Kubis termasuk dalam golongan tanaman sayuran semusim atau umur pendek. Tanaman ini hanya dapat dilakukan panen satu kali setelah itu akan mati. Pemanenan kubis dilakukan pada saat umur kubis mencapai 60-70 hari setelah tanam. </p>

<h1><u>Penyakit Pada Kubis Beserta Gejalanya</u></h1>
<p class="entry-content">
<p>> Akar Gada      = Daun layu, akar berjamur, daun pucat, pertumbuhan terhambat
<p>> Bercak Daun    = Daun bercak dan bintil, daun pucat, batang bergaris 
<p>> Busuk Basah    = Batang berwarna coklat, daun bercak dan bintil
<p>> Busuk Hitam    = Daun layu, akar rusak, daun pucat
<p>> Kaki Hitam     = Daun layu, akar rusak, batang berwarna coklat, daun bercak dan bintil
<p>> Mosaik         = Daun pucat, pertumbuhan terhambat
<p>> Tepung Berbulu = Batang berwarna coklat, daun bercak dan bintil, daun pucat